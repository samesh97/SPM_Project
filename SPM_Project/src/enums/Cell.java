package enums;

public class Cell
{
	private int cellH,cellV,day;

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getCellH() {
		return cellH;
	}

	public void setCellH(int cellH) {
		this.cellH = cellH;
	}

	public int getCellV() {
		return cellV;
	}

	public void setCellV(int cellV) {
		this.cellV = cellV;
	}
}
